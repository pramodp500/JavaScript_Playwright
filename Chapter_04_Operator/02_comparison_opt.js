// = -> Assignment Operator
// == -> Comparison Operator
// === -> Strict Comparison Operator

let a = 10;
let b = "10";

console.log(a == b);
console.log(a === b);

// > , < , >= , <= , ! ,!=
// Always will have boolean results
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
console.log(a != b);
console.log(a !== b);
